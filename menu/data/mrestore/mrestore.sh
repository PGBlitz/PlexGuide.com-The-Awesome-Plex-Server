# Recalls Important Variables
#mnt=$(cat /var/plexguide/server.hd.path)
restoreid=$(cat /var/plexguide/restore.id)
#blank out restore.Build
touch /opt/appdata/plexguide/restore.build
rm -rf /opt/appdata/plexguide/restore.build

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
↘️  NOTE: Checking Server [$restoreid]'s backups
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF

# Recalls List for Backup Operations
rclone ls gdrive:/plexguide/backup/$restoreid | awk '{ print $2 }' > /opt/appdata/plexguide/restore.list

### Builds Backup List - END
# Build up list backup list for the main.yml execution

while read p; do
  p=${p%.tar}
  echo -n $p >> /opt/appdata/plexguide/restore.build
  echo -n " " >> /opt/appdata/plexguide/restore.build
done </opt/appdata/plexguide/restore.list

# Just for the Restore Interace for display


# Execute Interface
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
↘️  LIST: Solo Restore >>> Via RClone - gdrive:/plexguide/backup/
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

☑️  Restoring all applications below. Certain apps that generated tons
of metadata can take quite a while (i.e. Plex, Sonarr, Radarr). Plex
alone can take 45min+.

EOF
echo "✅️  All Apps Being Restored: "
cat /opt/appdata/plexguide/restore.build

echo;
echo;
read -p '⚠️  Continue? Type >>> yes or no | Press [ENTER]: ' typed < /dev/tty

  if [ "$typed" == "" ]; then
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⛔️  WARNING! - You Must Type Yes or No!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

EOF
  sleep 3
  bash /opt/plexguide/menu/data/mrestore/mrestore.sh
  exit
elif [ "$typed" == "no" ]; then
  exit
elif [ "$typed" == "yes" ]; then
  a=a
else

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⛔️ WARNING! You Failed to type --- yes or no --- Restarting!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 5
bash /opt/plexguide/menu/data/mbackup/mbackup.sh
exit
fi
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅️  PASS: Starting the Mass Restore Process - This Will Take Awhile!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF

########################### Next Phase
while read "p"; do
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⚠️  NOTICE: Restoring - "$p"
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 2
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
↘️  NOTE: Determing File Size - $p
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF

size=$(rclone ls gdrive:/plexguide/backup/$restoreid | grep $p | awk '{ print $1 }' )
display=$(expr $size / 1000000)

  if [ "$display" == "0" ]; then
    display=1
  fi

echo $display > /var/plexguide/rclone.size

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅️  PASS: Restoring - $p | File Size: $display MB
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF

# Prevents From Repeating
sleep 4

########################### Next Phase
echo $p > /tmp/program_var

### For Folder Names for Restore
q=${p%.tar}
echo "$q" > /var/plexguide/restore.name
docker ps -a --format "{{.Names}}" | grep -c "\<$p\>" > /tmp/docker.check
ansible-playbook /opt/plexguide/menu/data/mrestore/mrestore.yml

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅️  PASS: $p - Process Complete - Moving On!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF

sleep 4
done </opt/appdata/plexguide/restore.list

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🌇  PASS: Process Complete!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

EOF
read -n 1 -s -r -p "Press [ANY] Key to Continue "
echo
exit
